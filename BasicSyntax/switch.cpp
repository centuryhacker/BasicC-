#include <iostream>

using namespace std;




int main()
{
    int value = 9;

    switch(value)
    {
    case 4:
        cout<<"value is four"<<endl;
        break;
    case 5:
        cout<<"value is five"<<endl;
        break;

    case 6:
        cout<<"value is six"<<endl;
        break;


    default:
        cout<<"Unrecognized value"<<endl;
        break;


    }

    return 0;
}
